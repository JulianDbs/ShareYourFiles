/**
 * This class is used as a Json body response used by the rest services.
 * @author JulianDbs
 */
public class JsonResponse {

    @JsonProperty("message")
    private String message = "";

    @JsonProperty("status")
    private int status = 0;

    @JsonProperty("data")
    private String data = "";

    public JsonResponse() {}

    public JsonResponse(String message, int status, String data) {
        this.message = message;
        this.status = status;
        this.data = data;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
